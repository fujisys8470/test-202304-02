package test;

public class JSample {
	public static void main(String[] args) {
		char c1 = 'a';

		System.out.println(c1); // a と出力
		System.out.println('\u0061'); // 97 と出力
		System.out.println("こんにちは\nお元気ですか");
		System.out.println(123);
		System.out.println(0x3F01);
		System.out.println(45 + 75);
		System.out.println(2.712e-3);
		System.out.println(7.5e-2);
		System.out.println(220_000000);
	}
}
